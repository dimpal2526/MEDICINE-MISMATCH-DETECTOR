import streamlit as st
import pandas as pd
import matplotlib.pyplot as plt

# -----------------------------
# Streamlit Config
# -----------------------------
st.set_page_config(page_title="ğŸ’Š Medicine Mismatch Detector", layout="wide")
st.title("ğŸ’Š Medicine Mismatch Detector Dashboard")

st.markdown("""
Upload your *medicine dataset* below to analyze:
- âœ… Matches vs âŒ Mismatches (tables + summary)
- ğŸ“Š Interactive Bar & Line charts
- ğŸ©º Disease-wise analysis
- ğŸ” Filtering by Disease & Status
""")

# -----------------------------
# File Upload
# -----------------------------
uploaded_file = st.file_uploader("ğŸ“‚ Upload your medicine dataset (CSV/Excel)", type=["csv", "xlsx"])

if uploaded_file is not None:
    # Load dataset
    if uploaded_file.name.endswith(".csv"):
        df = pd.read_csv(uploaded_file)
    else:
        df = pd.read_excel(uploaded_file)

    st.subheader("ğŸ“„ Dataset Preview")
    st.dataframe(df.head())

    # -----------------------------
    # Check for required columns
    # -----------------------------
    if {"Medicine1", "Medicine2", "Status", "Disease"}.issubset(df.columns):

        # -----------------------------
        # ğŸ” Filtering Options
        # -----------------------------
        st.sidebar.header("ğŸ” Filters")

        # Filter by Disease
        selected_disease = st.sidebar.selectbox("Filter by Disease", ["All"] + df["Disease"].unique().tolist())
        if selected_disease != "All":
            df = df[df["Disease"] == selected_disease]

        # Filter by Status
        status_filter = st.sidebar.radio("Filter by Status", ["All", "Match", "Mismatch"])
        if status_filter != "All":
            df = df[df["Status"] == status_filter]

        # -----------------------------
        # âœ… Match vs âŒ Mismatch Tables
        # -----------------------------
        st.subheader("ğŸ” Match vs Mismatch Medicines (Tables)")

        matched = df[df["Status"] == "Match"]
        mismatched = df[df["Status"] == "Mismatch"]

        col1, col2 = st.columns(2)

        with col1:
            st.success("âœ… Matched Medicines")
            st.dataframe(matched)

        with col2:
            st.error("âŒ Mismatched Medicines")
            st.dataframe(mismatched)

        # -----------------------------
        # ğŸ“Š Summary Counts
        # -----------------------------
        st.subheader("ğŸ“Š Match vs Mismatch Summary")
        status_count = df["Status"].value_counts()
        st.write(f"Total Records: {len(df)}")
        st.write(f"âœ… Matches: {status_count.get('Match',0)} | âŒ Mismatches: {status_count.get('Mismatch',0)}")

        # -----------------------------
        # ğŸ“Š Bar Chart
        # -----------------------------
        st.subheader("ğŸ“Š Match vs Mismatch (Bar Chart)")
        fig, ax = plt.subplots()
        status_count.plot(kind="bar", ax=ax, color=["green", "red"])
        ax.set_ylabel("Count")
        ax.set_title("Match vs Mismatch Medicines")
        st.pyplot(fig)

        # -----------------------------
        # ğŸ“ˆ Line Chart (Trend)
        # -----------------------------
        st.subheader("ğŸ“ˆ Match vs Mismatch Trend")
        df["StatusNumeric"] = df["Status"].apply(lambda x: 1 if x == "Match" else 0)
        st.line_chart(df["StatusNumeric"])

        # -----------------------------
        # ğŸ©º Disease Analysis
        # -----------------------------
        st.subheader("ğŸ©º Disease-wise Medicine Analysis")
        disease_count = df["Disease"].value_counts()
        fig2, ax2 = plt.subplots()
        disease_count.plot(kind="bar", ax=ax2, color="skyblue")
        ax2.set_ylabel("Count")
        ax2.set_title("Diseases Covered by Dataset")
        st.pyplot(fig2)

        # -----------------------------
        # ğŸ“Š Filtered Dataset
        # -----------------------------
        st.subheader("ğŸ“Š Filtered Dataset (After Applying Filters)")
        st.dataframe(df)

    else:
        st.error("âš  Dataset must contain columns: Medicine1, Medicine2, Status, Disease")